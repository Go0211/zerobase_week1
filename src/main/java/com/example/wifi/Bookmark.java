package com.example.wifi;

public class Bookmark {
    int wifiInfoNum;
    int bookmarkNum;
    String bookmark_name;
    String wifi_name;
    String input_date;

    public int getWifiInfoNum() {
        return wifiInfoNum;
    }

    public void setWifiInfoNum(int wifiInfoNum) {
        this.wifiInfoNum = wifiInfoNum;
    }

    public int getBookmarkNum() {
        return bookmarkNum;
    }

    public void setBookmarkNum(int bookmarkNum) {
        this.bookmarkNum = bookmarkNum;
    }

    public String getBookmark_name() {
        return bookmark_name;
    }

    public void setBookmark_name(String bookmark_name) {
        this.bookmark_name = bookmark_name;
    }

    public String getWifi_name() {
        return wifi_name;
    }

    public void setWifi_name(String wifi_name) {
        this.wifi_name = wifi_name;
    }

    public String getInput_date() {
        return input_date;
    }

    public void setInput_date(String input_date) {
        this.input_date = input_date;
    }
}
